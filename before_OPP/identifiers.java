package before_OPP;

public class identifiers {

    /*
     * An identifier in Java is the name given to Variables, Classes, Methods,
     * Packages, Interfaces, etc. These are the unique names and every Java
     * Variables must be identified with unique names.
     */

    public static void main(String[] arg) {

        int a = 20;
    }
    // here 5 identifier
    // identifier -> class name
    // main-> method name
    // String -> predefined class name
    // arg -> String array variable
    // a -> variable

    // identifiers rules
    /*
     * The only allowed characters for identifiers are all alphanumeric
     * characters([A-Z],[a-z],[0-9]), ‘$‘(dollar sign) and ‘_‘ (underscore). For
     * example “geek@” is not a valid Java identifier as it contains a ‘@’ a special
     * character.
     * Identifiers should not start with digits([0-9]). For example “123geeks” is
     * not a valid Java identifier.
     * Java identifiers are case-sensitive.
     * There is no limit on the length of the identifier but it is advisable to use
     * an optimum length of 4 – 15 letters only.
     * Reserved Words can’t be used as an identifier. For example “int while = 20;”
     * is an invalid statement as a while is a reserved word. There are 53 reserved
     * words in Java.
     */

    // Reserved word:
    // Amy programming language reserves some words to represents funtionalities,
    // defined by that language.These words are called reserved words..

    // Keywords:
    // keywords are the reserved words in a programming language that used for some
    // internal process or represents some predefined actions. these words are
    // therefore not allowed to use as identifiers

    // keywords 'const' & 'goto' are reserved ,even though they are not currently in
    // use.
    // true, false & null are literals..they still can't used as identifiers in a
    // program.
}
